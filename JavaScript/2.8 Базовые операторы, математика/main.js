"" + 1 + 0 // 10 
//сначала "" + 1 приведется к строкам и получится просто '1' (строка)
//потом '1' + 0 тоже приводятся к строке и будет '10'

"" - 1 + 0 //-1
//сначала '' - 1 = -1 (число) т.к это не сложение и приведется к числам 0-1=-1

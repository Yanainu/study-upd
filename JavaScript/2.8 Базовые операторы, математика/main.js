true + false // 1 т.к. это 1 + 0

6 / "3" // 2 т.к. не сложение и 3 приведется к число 6/3=2
 
"2" * "3" // 6 т.к .не сложение и приведется к числам 2*3=6

4 + 5 + "px" // 9px
//сначала 4+5 будет 9, т.к. это уже числа. потом 9+'px' = 9px т.к. сложение и 9 приведется к строке

"$" + 4 + 5// $45
//сначала "$"+4 будет "$4", потом "$4"+5 будет "$45" ну сложение и все к строке

"4" - 2 //2
//пушто не сложение - приводит 4 к числу, 4-2 = 2

"4px" - 2 // NaN
//тут должно приводиться к числу все но 4px не привести поэтому NaN

